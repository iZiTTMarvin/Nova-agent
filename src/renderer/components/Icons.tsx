/** 折叠指示器图标 - 工具结果展开与折叠 */
export const ChevronIcon: React.FC<IconProps & { direction?: 'up' | 'down' | 'right' }> = ({ size = 16, direction = 'down', ...props }) => {
  const getRotation = () => {
    switch (direction) {
      case 'up': return 'rotate(180deg)'
      case 'right': return 'rotate(-90deg)'
      default: return 'rotate(0deg)'
    }
  }

  return (
    <svg 
      width={size} 
      height={size} 
      viewBox="0 0 24 24" 
      fill="none" 
      stroke="currentColor" 
      strokeWidth="2" 
      strokeLinecap="round" 
      strokeLinejoin="round" 
      style={{ transform: getRotation(), transition: 'transform 0.2s ease', ...props.style }}
      {...props}
    >
      <polyline points="6 9 12 15 18 9" />
    </svg>
  )
}

